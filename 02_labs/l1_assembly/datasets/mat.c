#include <stdio.h>
#define DIM 10

int main() {
    int A[DIM][DIM] = {
        {0,3,2,0,3,1,0,3,2,3},
        {2,0,3,3,1,2,3,0,0,1},
        {1,1,2,3,1,2,3,1,1,3},
        {2,2,0,1,3,2,2,2,0,0},
        {1,0,1,3,3,0,3,3,3,3},
        {0,3,2,1,2,2,0,0,3,0},
        {1,1,0,3,3,1,2,3,3,0},
        {1,2,1,0,1,2,2,1,0,3},
        {1,0,2,2,1,1,1,1,1,1},
        {2,0,3,1,1,2,2,3,3,1}
    };

    int B[DIM][DIM] = {
        {1,1,0,3,1,2,0,0,0,0},
        {0,2,1,2,3,0,0,3,3,2},
        {2,1,2,3,3,0,2,2,1,1},
        {2,2,0,2,2,1,2,3,2,2},
        {3,3,2,2,1,1,1,1,2,1},
        {2,2,3,3,3,0,0,3,2,3},
        {2,3,1,2,1,1,2,2,0,1},
        {0,3,2,1,1,1,2,0,1,2},
        {2,0,2,1,3,3,2,3,2,0},
        {3,1,3,3,2,0,1,0,1,1}
    };

    long long C[DIM][DIM] = {0};

    for (int i=0;i<DIM;i++)
        for (int j=0;j<DIM;j++)
            for (int k=0;k<DIM;k++)
                C[i][j] += (long long)A[i][k] * B[k][j];

    for (int i=0;i<DIM;i++) {
        for (int j=0;j<DIM;j++) printf("%lld%c", C[i][j], (j==DIM-1)?'\n':' ');
    }
    return 0;
}
